#slicing /selecting subsequences
lang = "python"
#syntax - [start arguement: stop arguement:step]
print("Vanshika"[2:5])     #output= nsh
print("Vanshika"[2:5:1])   #output= nsh
print("Vanshika"[2:5:2])   #output= nh   
print("Vanshika"[0::2])    #output= vnhk
print("Vanshika"[8::-1])   #output = akihsnaV
print("Vanshika"[::-1])    #output = akihsnaV (TRICK)